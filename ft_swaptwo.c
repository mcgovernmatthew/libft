#include "libft.h"

void			ft_swaptwo(int *x, int *y, int *x1, int *y1)
{
	int			c;
	int			d;

	c = *x;
	*x = *y;
	*y = c;
	d = *x1;
	*x1 = *y1;
	*y1 = d;
}
